
class Usuario():
    def __init__(self, nome, email, senha, tipo):
        self.nome = nome
        self.email = email
        self.senha = senha
        self.tipo = tipo

class Produto():
    def __init__(self, codigo, nome, tipo, preco):
        self.codigo = codigo
        self.nome = nome
        self.tipo = tipo
        self.preco = preco
        








